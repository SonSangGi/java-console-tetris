package dev.sanggi.tetris.screen;

/**
 * @@author SonSangGi
 * @created 2020/03/14
 */
public class ScreenConfig {
    public int WIDTH;
    public int HEIGHT;
    public int X;
    public int Y;

    public ScreenConfig(int WIDTH, int HEIGHT, int X, int Y) {
        this.WIDTH = WIDTH;
        this.HEIGHT = HEIGHT;
        this.X = X;
        this.Y = Y;
    }
}
